     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $product = new Product();
        $product->Product_Name=$request->productName;
        $product->Product_Category = $request->productCategory;
        $product->Product_EmbedCode = $request->productEmbedCode;
        $product->Product_Description =  $request->descr;
        $product->isRecommended = $request->productRecommended;
        $product->save();
        return redirect(url('/backend/addProduct'));

    }

